package com.niit.modal;

public class RegistrationDetails 
{
       
    private String fname,lname,email,username,password,age,mobile,alt_mobile,gender,address;    

    
    
    public String getFname() 
    {
        return fname;
    }

    public void setFname(String fname) 
    {
        this.fname = fname;
    }
   
    public String getLname() 
    {
        return lname;
    }

    public void setLname(String lname) 
    {
        this.lname = lname;
    }
   
    public String getEmail() 
    {
        return email;
    }

    public void setEmail(String email) 
    {
        this.email = email;
    }
   
    public String getUsername() 
    {
        return username;
    }

    public void setUsername(String username) 
    {
        this.username = username;
    }
   
    public String getPassword() 
    {
        return password;
    }

    public void setPassword(String password) 
    {
        this.password = password;
    }
   
    public String getAge() 
    {
        return age;
    }

    public void setAge(String age) 
    {
        this.age = age;
    }
   
    public String getMobile() 
    {
        return mobile;
    }

    public void setMobile(String mobile) 
    {
        this.mobile = mobile;
    }
    
    public String getAlt_mobile() 
    {
        return alt_mobile;
    }

    public void setAlt_mobile(String alt_mobile) 
    {
        this.alt_mobile = alt_mobile;
    }
   
    public String getGender() 
    {
        return gender;
    }

    public void setGender(String gender) 
    {
        this.gender = gender;
    }
   
    public String getAddress() 
    {
        return address;
    }

    public void setAddress(String address) 
    {
        this.address = address;
    }
}
